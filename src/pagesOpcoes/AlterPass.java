    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        String sql = "select senha from usuarios where login like ?";
        try {
            pst = conexao.prepareStatement(sql);
            String getUser = username.getText();
            String getPass = txPassAntiga.getText();
            pst.setString(1, getUser);
            rs = pst.executeQuery();

            while (rs.next()) {
                String Senha = rs.getString(1);
                String getSenha = txNewPass.getText();
                String getSenhaRe = txNewPassRE.getText();

                if (getPass.equals(Senha)) {

                    if (getSenha.equals(getSenhaRe)) {
                        String sqlU = "update usuarios set senha=?  where login=?";
                        try {
                            pst = conexao.prepareStatement(sqlU);
                            pst.setString(1, txNewPass.getText());
                            pst.setString(2, username.getText());

                            if ((txPassAntiga.getText().isEmpty())) {
                                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios!!");

                            } else {
                                int adicionado = pst.executeUpdate();

                                if (adicionado > 0) {
                                    Notification noti = new Notification(this, Notification.Type.SUCCESS, Notification.Location.TOP_CENTER, "Senha alterada com sucesso!!!");
                                    noti.showNotification();

                                }
                            }
                        } catch (HeadlessException | SQLException e) {
                            JOptionPane.showMessageDialog(null, e);
                        }
                    } else {
                        Notification noti = new Notification(this, Notification.Type.WARNING, Notification.Location.TOP_CENTER, "As senhas não conhecidem!!!");
                        noti.showNotification();
                    }

                } else {
                    Notification noti = new Notification(this, Notification.Type.WARNING, Notification.Location.TOP_CENTER, "Senha antiga incorreta!!!");
                    noti.showNotification();
                }

            }

        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }


    }//GEN-LAST:event_btSalvarActionPerformed

