    public void adicionar() {

        String sql = "insert into etiquetas(etiqueta,corhex,corrgb) values(?,?,?)";
        try {
            pst = conexao.prepareStatement(sql);

            pst.setString(1, textEtiqueta.getText());
            pst.setString(2, txtColor.getText());
            pst.setString(3, txtColor.getText());

            if ((textEtiqueta.getText().isEmpty())) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatÃ³rios!!");

            } else {
                int adicionado = pst.executeUpdate();

                if (adicionado > 0) {
                    listaG.clear();
                    ListarDados();

                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao adicionar!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void ListarDados() {
        String sql = "select etiqueta from etiquetas order by etiqueta";

        try {
            pst = conexao.prepareStatement(sql);

            rs = pst.executeQuery();

            while (rs.next()) {
                String nome = rs.getString(1);
                listaG.addElement(nome);
                ListaEtiqueta.setModel(listaG);

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }
    }

