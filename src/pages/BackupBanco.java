    private void BtBackupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtBackupActionPerformed
        // TODO add your handling code here:

        if (name.getText().equals("")) {
            ImageIcon icon = new ImageIcon(Toolkit.getDefaultToolkit().getImage(getClass().getResource("/src/cancelar.png")));
            JOptionPane.showMessageDialog(null, "<html><b>Adicione um nome ao backup!!</b></html>", "<html><b>ERROR Service!</b></html>", JOptionPane.ERROR_MESSAGE, icon);

        } else {
            try {
                String archiveName = name.getText();
                String mysqlPath = "C:\\Program Files\\MySQL\\MySQL Server 8.0\\bin\\";
                String backupPath = "C:\\convtec\\BACKUP\\" + archiveName + ".sql";
                String dbName = "docate36_supplyx";
                String dbUser = "admin";
                String dbPass = "87520213@Server";
                String dbHost = "DESKTOP-0TPE7OL";
                String executeCmd = mysqlPath + "mysqldump -h " + dbHost + " -u " + dbUser + " -p" + dbPass + " --add-drop-database -B " + dbName + " -r " + backupPath;
                Process runtimeProcess = Runtime.getRuntime().exec(executeCmd);
                int processComplete = runtimeProcess.waitFor();

                if (processComplete == 0) {
                    Notification noti = new Notification(this, Notification.Type.SUCCESS, Notification.Location.TOP_CENTER, "backup completo!!");
                    noti.showNotification();
                } else {
                    Notification noti = new Notification(this, Notification.Type.WARNING, Notification.Location.TOP_CENTER, "Falha no backup!!");
                    noti.showNotification();
                }
            } catch (IOException | InterruptedException e) {
                JOptionPane.showMessageDialog(null, e, "Erro!", 2);
            }
        }


    }//GEN-LAST:event_BtBackupActionPerformed

